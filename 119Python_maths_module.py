# Maths without maths module 

# min/ max functions()
x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

# absolute function() value of any number
a = abs(-7.25)

print(a)

# finding 4**3 power() function
x = pow(4, 3)

print(x)


import math

# b = math. will give all the functions and methods available in math module
b = math.sqrt(64)
print(b)

# The math.ceil() method rounds a number upwards to
# its nearest integer, and the math.floor()
# method rounds a number downwards to its nearest integer, and returns the result:

c = math.ceil(1.4)
d = math.floor(1.4)
print(c)
print(d)

pi = math.pi
print(round(pi, 2))