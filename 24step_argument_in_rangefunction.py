#step argument in range
for i in range(0,11):
    print(i)

for i in range(0,11,1):
    print(i)
#both above code are same by default step is taken as 1

#even number
for i in range(0,11,2):
    print(f"even number: {i}")

#reverse order
for i in range (10, -1, -1):
    print (i)

#negative numbers
for i in range(1, -11, -1):
    print(i)
